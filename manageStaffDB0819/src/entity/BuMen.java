package entity;

public class BuMen {
	private int id;
	private String name;
	private int staNums;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getStaNums() {
		return staNums;
	}
	public void setStaNums(int staNums) {
		this.staNums = staNums;
	}
}
