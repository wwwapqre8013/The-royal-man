package entity;

public class Shi {
private int id;
private String name;
private Sheng sheng;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Sheng getSheng() {
	return sheng;
}
public void setSheng(Sheng sheng) {
	this.sheng = sheng;
}


}